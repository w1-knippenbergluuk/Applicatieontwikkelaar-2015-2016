                        <h2>Statement</h2>

                        <p>
                            “Hoe dingen samen worden”
                        </p>
                        <p>
                            De dagelijkse verwondering, ervaringen in de directe omgeving geven mij inspiratie, 
                            zo niet “dwingen” mij elke dag verslag te doen van grote en kleine gebeurtenissen.
                        </p>
                        <p>
                            Tijdens wandelingen neem ik waar, verzamel materiaal, objecten en geluiden, teken en vertaal en speel met hun betekenissen. 
                            Dit levert afwisselend concrete of meer associatieve tekeningen, foto’s (polaroid) of concepten op voor (monumentaal) ruimtelijk werk. 
                            Dit komt in mijn atelier samen met de niet-tastbare werkelijkheid zoals gedachten en herinneringen. 
                            Zo komen “dingen samen” en zijn ze de bron van een relatie tussen plaats en tijd.
                        </p>
                        <p>
                            Schilderijen ontstaan vanuit gedachteschetsen en worden vervolgens vanuit een lijnenspel gevormd tot een 
                            compositie waarin gelaagdheid en handschrift belangrijk zijn. 
                            Kleuren zijn uitbundig of ingetogen en de verf ligt in transparante lagen op het doek. 
                            Er vormt zich een opeenstapeling van beelden. Zij ontstaan in een proces van voortdurende verandering en groei.
                        </p>
                        <p>
                            Disciplines: tekenen, schilderen, mixed media, conceptueel/ monumentaal, (Ruimtelijke) werken , installaties (vrij, projectmatig of in opdracht).
